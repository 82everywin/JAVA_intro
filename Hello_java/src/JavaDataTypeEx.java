public class JavaDataTypeEx {

	public static void main(String args[]) {
		/*
		 * Java Data Type 
		 * 
		 * 데이터 타입이란 목적에 맞게 연산 등을 할 수 있도록 지정된 타입들을 뜻합니다.
		 * (여러분의 마지막 봤던 핸드폰 앱을 연상하세요..)
		 * 
		 * 1. 크게 두가지로 나뉘어 집니다. 
		 *  1.1  객체타입(Reference Type) --> 이거 배우려고 오신겁니다. 
		 *  1.2 Primitive Type --> 객체타입이 아닌 나머지들...
		 *  
		 *  Ptype의 종류
		 *  
		 *  1. 수치형 중 정수형  : byte(1byte), short(2byte), int(4byte default 정수 타입), long(8byte) 
		 *  2. 수치형 중 실수형 : float(4byte), double(8byte default 실수 타입)
		 *  3. 문자형(문자열아님) : char(2byte)
		 *  4. boolean : True or False 
		 *  
		 * 
		 */
		
		System.out.println(Integer.toBinaryString(15));
		System.out.println(Byte.MAX_VALUE);// byte의 가장 큰 값 출력.
		System.out.println(Byte.MIN_VALUE);// byte의 가장 큰 값 출력.
		
		/*
		 * 정수형 데이터 : 연산을 위해서 준비된 데이터입니다. 
		 * 
		 */
		
		System.out.println((int)'A');
		
		
		

		
		
		
		
		
		
		
		
		
		
		
		
		
		}

}
